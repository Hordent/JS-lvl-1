'use strict';

/**
 *Сумма двух чисел
 * @param {number} a первое число
 * @param {number} b второе число
 * @returns
 */
function add (a, b) {
    return a + b;
}

/**
 *Разность двух чисел
 *
 * @param {number} a первое число
 * @param {number} b второе число
 * @returns
 */
function sub (a, b) {
    return a - b;
}

/**
 *Произведение двух чисел
 *
 * @param {number} a первое число
 * @param {number} b второе число
 * @returns
 */
function mult (a, b) {
    return a * b;
}

/**
 *Частное двух чисел
 *
 * @param {number} a первое число
 * @param {number} b второе число
 * @returns
 */
function div (a, b) {
    return a / b;
}